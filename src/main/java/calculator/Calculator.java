package calculator;

public class Calculator {


    public double plus(double prev, double next) {
        return prev + next;
    }


    public double multiply(double prev, double next) {
        return prev * next;
    }

    public double minus(double prev, double next) {
        return prev - next;
    }

    public double divide(double prev, double next) {
        return prev / next;
    }
}
